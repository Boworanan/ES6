let x = 5;
let y = 3;

//addition
console.log(`x + y = ${x + y}`)

//subtraction
console.log(`x - y = ${x - y}`)

//muliplication
console.log(`x * y = ${x * y}`)

//remainder
console.log(`x % y = ${x % y}`)

//increment
console.log(`++x = ${++x}`)
console.log(`x++ = ${x++}`)
console.log(`x = ${x}`)

//drecrement
console.log(`--y = ${--y}`)
console.log(`y-- = ${y--}`)
console.log(`y = ${y}`)